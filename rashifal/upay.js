function loadUpay() {
    const container = document.getElementById('upay-container');
    container.innerHTML = '';

    upayData.forEach((item) => {
        const card = document.createElement('div');
        card.className = 'card';
        card.innerHTML = `
            <div class="rashi-icon">${item.icon}</div>
            <h3>${item.rashi}</h3>
            <p>${item.upay}</p>
        `;
        container.appendChild(card);
    });
}

window.onload = loadUpay;